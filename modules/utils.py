import re
from typing import List


_TOKEN_PATTERN = re.compile(r'[а-яёА-ЯЁa-zA-Z]{2,}')
_STOP_WORDS = {
    'это','что','как','так','вот','ещё','уже','было','будет',
    'когда','потом','тогда','здесь','там','тут','все','весь',
    'очень','совсем','опять','снова','чтобы','тоже','даже',
    'только','почти','прямо','себе','сам','сама','сами',
    'меня','тебя','него','нее','них','нам','вам','им',
    'мой','твой','наш','ваш','свой','его','её','их',
    'этот','тот','такой','такая','такие','какой','какая',
    'можно','нужно','надо','может','могут','мог','могла',
    'есть','был','была','были','буду','будешь','будет',
    'сказал','сказала','говорит','говорил','думаю','думал',
    'просто','вообще','конечно','ладно','хорошо','плохо',
}

def _tokenize(text: str) -> List[str]:
    return [w.lower() for w in _TOKEN_PATTERN.findall(text)
            if w.lower() not in _STOP_WORDS and len(w) > 2]


def _clamp(v: float, lo: float = 0.0, hi: float = 1.0) -> float:
    if v < lo:
        return lo
    if v > hi:
        return hi
    return v
